package com.ani.ui.view;

import android.graphics.drawable.Drawable;

public class ConsumptionItem {

	float up, down;
	Drawable icon;
	long time;
	String dest, name;

	public ConsumptionItem(float up, float down, long time, Drawable icon,
			String dest, String name) {
		super();
		this.up = up;
		this.down = down;
		this.time = time;
		this.icon = icon;
		this.dest = dest;
		this.name = name;
	}

	public float getUp() {
		return up;
	}

	public void setUp(float up) {
		this.up = up;
	}

	public float getDown() {
		return down;
	}

	public void setDown(float down) {
		this.down = down;
	}

	public long getTime() {
		return time;
	}

	public void setTime(long time) {
		this.time = time;
	}

	public String getDest() {
		return dest;
	}

	public void setDest(String dest) {
		this.dest = dest;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Drawable getIcon() {
		return icon;
	}

	public void setIcon(Drawable icon) {
		this.icon = icon;
	}
}
